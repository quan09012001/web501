// Cú pháp cơ bản
let message1 = `Hello word!`;
console.log(message1); // output: Hello world!
console.log(typeof message1); // output: string

// Nếu muốn sử dụng ký tự đặc biệt chỉ cần thêm \ phía trước
let message2 = `\`Hello\` world!`;
console.log(message2); // output: `Hello` world!
